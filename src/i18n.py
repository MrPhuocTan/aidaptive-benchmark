def normalize_lang(value: str | None) -> str:
    if not value:
        return "en"
    candidate = value.strip().lower()
    if candidate.startswith("zh"):
        return "zh"
    if candidate.startswith("vi"):
        return "vi"
    if candidate.startswith("en"):
        return "en"
    return "en"


def translate(lang: str, key: str, **kwargs: Any) -> str:
    lang = normalize_lang(lang)
    text = TRANSLATIONS.get(lang, {}).get(key) or TRANSLATIONS["en"].get(key) or key
    if kwargs:
        try:
            return text.format(**kwargs)
        except Exception:
            return text
    return text


def get_client_translations(lang: str) -> dict[str, str]:
    lang = normalize_lang(lang)
    return {
        key: translate(lang, key)
        for key in TRANSLATIONS["en"]
        if key.startswith("js.") or key.startswith("common.") or key.startswith("benchmark.")
    }
